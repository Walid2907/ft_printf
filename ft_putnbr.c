#include "printf.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(1, "-2147483648", 11);
			return ;
		}
		else
		{
			write(1, "-", 1);
			n = -n;
		}
	}
	if (n >= 10)
		ft_putnbr((n / 10));
	ft_putchar((n % 10 + '0'));
}
